class BSTNode:
    """
    Binary Search Tree Node class
    DO NOT CHANGE THIS CLASS IN ANY WAY
    """

    def __init__(self, value: object) -> None:
        """
        Initialize a new BST node
        DO NOT CHANGE THIS METHOD IN ANY WAY
        """
        self.value = value   # to store node's data
        self.left = None     # pointer to root of left subtree
        self.right = None    # pointer to root of right subtree

    def __str__(self) -> str:
        """
        Override string method
        DO NOT CHANGE THIS METHOD IN ANY WAY
        """
        return 'BST Node: {}'.format(self.value)


class BST:
    """
    Binary Search Tree class
    """

    def __init__(self, start_tree=None) -> None:
        """
        Initialize new Binary Search Tree
        DO NOT CHANGE THIS METHOD IN ANY WAY
        """
        self._root = None

        # populate BST with initial values (if provided)
        # before using this feature, implement add() method
        if start_tree is not None:
            for value in start_tree:
                self.add(value)

    def __str__(self) -> str:
        """
        Override string method; display in pre-order
        DO NOT CHANGE THIS METHOD IN ANY WAY
        """
        values = []
        self._str_helper(self._root, values)
        return "BST pre-order { " + ", ".join(values) + " }"

    def _str_helper(self, node: BSTNode, values: []) -> None:
        """
        Helper method for __str__. Does pre-order tree traversal
        DO NOT CHANGE THIS METHOD IN ANY WAY
        """
        if not node:
            return
        values.append(str(node.value))
        self._str_helper(node.left, values)
        self._str_helper(node.right, values)

    def get_root(self) -> BSTNode:
        """
        Return root of tree, or None if empty
        DO NOT CHANGE THIS METHOD IN ANY WAY
        """
        return self._root

    def is_valid_bst(self) -> bool:
        """
        Perform pre-order traversal of the tree.
        Return False if nodes don't adhere to the bst ordering property.

        This is intended to be a troubleshooting method to help find any
        inconsistencies in the tree after the add() or remove() operations.
        A return of True from this method doesn't guarantee that your tree
        is the 'correct' result, just that it satisfies bst ordering.

        DO NOT CHANGE THIS METHOD IN ANY WAY
        """
        stack = Stack()
        stack.push(self._root)
        while not stack.is_empty():
            node = stack.pop()
            if node:
                if node.left and node.left.value >= node.value:
                    return False
                if node.right and node.right.value < node.value:
                    return False
                stack.push(node.right)
                stack.push(node.left)
        return True

    # ------------------------------------------------------------------ #

    def add(self, value: object) -> None:
        """
        Adds a new value to the tree. Duplicates are allowed. If a value already exists, the next version will be placed next to it.
        """
        if self._root is None:
            # BST is empty, set new node as root
            self._root = BSTNode(value)
            return
        # Traverse the tree to find the appropriate position to insert the new node
        current_node = self._root
        while True:
            if value < current_node.value:
                # Move to the left child
                if current_node.left is None:
                    # Found the appropriate position to insert the new node
                    current_node.left = BSTNode(value)
                    break
                else:
                    current_node = current_node.left
            else:
                # Move to the right child
                if current_node.right is None:
                    # Found the appropriate position to insert the new node
                    current_node.right = BSTNode(value)
                    break
                else:
                    current_node = current_node.right

    def remove(self, value: object) -> bool:
        """
        Removes a value from the tree. Returns True if value is removed
        """
        parent = None
        node = self._root
        while node and node.value != value:
            parent = node
            if value < node.value:
                node = node.left
            else:
                node = node.right
        if not node:
            # Value not found in tree
            return False

        if not node.left and not node.right:
            # Node has zero children
            if not parent:
                # Removing root node
                self._root = None
            elif node.value < parent.value:
                parent.left = None
            else:
                parent.right = None
        elif not node.left or not node.right:
            # Node has one child
            child = node.left if node.left else node.right
            if not parent:
                # Removing root node
                self._root = child
            elif node.value < parent.value:
                parent.left = child
            else:
                parent.right = child
        else:
            # Node has two children
            succ_parent = node
            succ = node.right
            while succ.left:
                succ_parent = succ
                succ = succ.left
            node.value = succ.value
            if succ_parent.left == succ:
                succ_parent.left = succ.right
            else:
                succ_parent.right = succ.right

        return True

    def contains(self, value: object) -> bool:
        """
        Returns True if the tree contains the value, False otherwise.
        """
        if self._root is None:
            return False

        stack = Stack()
        stack.push(self._root)

        while not stack.is_empty():
            node = stack.pop()
            if node.value == value:
                return True
            elif value < node.value and node.left is not None:
                stack.push(node.left)
            elif value > node.value and node.right is not None:
                stack.push(node.right)

        return False

    def inorder_traversal(self) -> Queue:
        """
        This method will perform an inorder transverse of the tre and return an object of Queue that tells the value of each node.
        """
        queue = Queue()
        stack = Stack()
        curr = self._root

        while curr is not None or not stack.is_empty():
            while curr is not None:
                stack.push(curr)
                curr = curr.left

            curr = stack.pop()
            queue.enqueue(curr.value)
            curr = curr.right

        return queue

    def find_min(self) -> object:
        """
        Returns lowest value of the Tree
        """
        if self._root is None:
            return None

        curr = self._root
        while curr.left is not None:
            curr = curr.left

        return curr.value

    def find_max(self) -> object:
        """
        Returns maximum value of the tree
        """
        if self._root is None:
            return None

        curr = self._root
        while curr.right is not None:
            curr = curr.right

        return curr.value

    def is_empty(self) -> bool:
        """
        Checks if the Tree is empty.
        """
        if self._root is None:
            return True
        return False

    def make_empty(self) -> None:
        """
        Erases the Tree.
        """
        self._root = None


